import { recursiveBacktracing, primsAlgorithm, ellersAlgorithm } from '../../src/maze-generator/maze-generator';

const parameters = [
    [5, 5, 0, 0, 4, 4], 
    [5, 5, 1, 2, 3, 4], 
    [10, 10, 0, 0, 9, 9], 
    [10, 10, 1, 2, 7, 8], 
    [12, 12, 0, 0, 11, 11], 
    [12, 12, 1, 2, 9, 10], 
    [14, 14, 0, 0, 13, 13],
    [14, 14, 1, 2, 11, 12]
];

describe('Maze Generator Tests', () => {
    test('recursiveBacktracking test case', () => {
        for (let [width, height, startRow, startCol, endRow, endCol] of parameters) {
            const info = "[width, height, startRow, startCol, endRow, endCol] = " + [width, height, startRow, startCol, endRow, endCol];
            const mazeGenerated = recursiveBacktracing(width, height, startRow, startCol, endRow, endCol);
            expect(mazeGenerated.length).toEqual(2*height-1);
            for (let i=0; i<mazeGenerated.length; i++) {
                expect(mazeGenerated[i].length).toEqual(2*width-1);
            }
            expect(mazeGenerated[2*startRow][2*startCol]).toEqual(2);
            expect(mazeGenerated[2*endRow][2*endCol]).toEqual(3);
            printMaze(mazeGenerated, info);
        }
    });
    test('primsAlgorithm test case', () => {
        for (let [width, height, startRow, startCol, endRow, endCol] of parameters) {
            const info = "[width, height, startRow, startCol, endRow, endCol] = " + [width, height, startRow, startCol, endRow, endCol];
            const mazeGenerated = primsAlgorithm(width, height, startRow, startCol, endRow, endCol);
            expect(mazeGenerated.length).toEqual(2*height-1);
            for (let i=0; i<mazeGenerated.length; i++) {
                expect(mazeGenerated[i].length).toEqual(2*width-1);
            }
            expect(mazeGenerated[2*startRow][2*startCol]).toEqual(2);
            expect(mazeGenerated[2*endRow][2*endCol]).toEqual(3);
            printMaze(mazeGenerated, info);
        }
    });
    test('ellersAlgorithm test case', () => {
        for (let [width, height, startRow, startCol, endRow, endCol] of parameters) {
            const info = "[width, height, startRow, startCol, endRow, endCol] = " + [width, height, startRow, startCol, endRow, endCol];
            // const probability = Math.random();
            const mazeGenerated = ellersAlgorithm(width, height, startRow, startCol, endRow, endCol);
            expect(mazeGenerated.length).toEqual(2*height-1);
            for (let i=0; i<mazeGenerated.length; i++) {
                expect(mazeGenerated[i].length).toEqual(2*width-1);
            }
            expect(mazeGenerated[2*startRow][2*startCol]).toEqual(2);
            expect(mazeGenerated[2*endRow][2*endCol]).toEqual(3);
            printMaze(mazeGenerated, info);
        }
    });
});

function printMaze(maze, info) {
    var s = info + "\n[\n";
    for (let i=0; i<maze.length; i++) {
        var t = "\t[";
        for (let j=0; j<maze[i].length; j++) {
            t += maze[i][j] + ", ";
        }
        t += "], \n";
        s += t;
    }
    s += "]\n";
    console.log(s)
}

